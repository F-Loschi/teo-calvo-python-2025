'''
Existem funções pré-prontas no Python que podem ser utilizadas para realizar diversas tarefas.
Elas são chamadas de funções built-in (funções embutidas) e podem ser utilizadas a qualquer momento.
Podemos também criar nossas próprias funções para realizar tarefas específicas.
Para criar uma função, utilizamos a palavra reservada def seguida do nome da função e parênteses.
'''
#%%
def f(x):
    return 1+x

# %%
f(0)
f(10)
# %%
